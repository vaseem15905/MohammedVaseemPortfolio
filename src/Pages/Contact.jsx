import React, { useState } from 'react';
import emailjs from 'emailjs-com';
import { db, addDoc, collection } from '../Configuration/firebaseConfig'; // Import from your firebase.js

// Initialize EmailJS with your public key
emailjs.init(import.meta.env.VITE_EMAILJS_USER_ID);

function Contact() {
  const [formData, setFormData] = useState({
    name: '',
    email: '',
    subject: '',
    message: ''
  });

  const [isPopupVisible, setIsPopupVisible] = useState(false); // State for popup visibility

  // Handle form input changes
  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData({
      ...formData,
      [name]: value
    });
  };

  // Handle form submission
  const handleSubmit = async (e) => {
    e.preventDefault();

    const templateParams = {
      name: formData.name,
      email: formData.email,
      subject: formData.subject,
      message: formData.message
    };

    try {
      // 1. Save form data to Firebase Firestore
      await addDoc(collection(db, 'PortfolioContacts'), formData);

      // 2. Send email via EmailJS
      await emailjs.send(import.meta.env.VITE_EMAILJS_SERVICE_ID, import.meta.env.VITE_EMAILJS_TEMPLATE_ID, templateParams);

      // Reset form data after successful submission
      setFormData({
        name: '',
        email: '',
        subject: '',
        message: ''
      });

      // Show the success popup
      setIsPopupVisible(true);
    } catch (error) {
      console.error('Error submitting form: ', error);
    }
  };

  // Close the popup
  const closePopup = () => {
    setIsPopupVisible(false);
  };

  return (
    <div id="contact" className="h-screen flex flex-col items-center justify-center relative px-6 sm:px-12 lg:px-24 py-10">
      {/* Contact Heading */}
      <h1 className="text-5xl sm:text-6xl lg:text-7xl text-[#00ff2f] font-bold text-center my-10 font-lexend z-10">
        Contact
      </h1>
      <br />

      {/* Contact Form */}
      <form
        onSubmit={handleSubmit}
        className="flex flex-col gap-8 bg-gradient-to-t from-[#259c2ba4] via-[#259c2b5c] to-[#259c2b1d] shadow-[0_0_2px_rgb(0,255,47)] p-8 sm:p-10 lg:p-12 rounded-3xl w-full max-w-4xl z-10"
      >
        {/* Name and Email Fields */}
        <div className="flex flex-col sm:flex-row gap-6 sm:gap-10 text-lg sm:text-xl">
          <input
            type="text"
            name="name"
            value={formData.name}
            onChange={handleChange}
            className="flex-1 p-4 rounded-md bg-transparent shadow-customInner text-white focus:outline-none selection:text-black selection:bg-[#259c2bc2] autofill:bg-transparent autofill:text-white"
            placeholder="Name"
          />
          <input
            type="email"
            name="email"
            value={formData.email}
            onChange={handleChange}
            className="flex-1 p-4 rounded-md bg-transparent shadow-customInner text-white focus:outline-none selection:text-black selection:bg-[#259c2bc2] autofill:bg-transparent autofill:text-white"
            placeholder="E-Mail"
          />
        </div>

        {/* Subject Field */}
        <input
          type="text"
          name="subject"
          value={formData.subject}
          onChange={handleChange}
          className="w-full p-4 rounded-md bg-transparent shadow-customInner text-white focus:outline-none selection:text-black selection:bg-[#259c2bc2] autofill:bg-transparent autofill:text-white"
          placeholder="Subject"
        />

        {/* Message Field */}
        <textarea
          name="message"
          value={formData.message}
          onChange={handleChange}
          className="w-full p-4 h-32 rounded-md bg-transparent shadow-customInner text-white focus:outline-none selection:text-white selection:bg-[#259c2bc2] autofill:bg-transparent autofill:text-white"
          placeholder="Write Something..."
        ></textarea>

        {/* Buttons */}
        <div className="flex flex-col sm:flex-row justify-between gap-4 mt-4">
          <button
            type="reset"
            className="p-3 text-[#00ff2f] rounded-md shadow-[0_0_1px_rgb(0,255,47)] bg-[#062203] hover:shadow-[0_0_20px_rgb(0,255,47)] hover:scale-110 hover:bg-[#00ff2f] hover:text-black transition-all"
          >
            Clear
          </button>
          <button
            type="submit"
            className="p-3 text-black rounded-md shadow-[0_0_1px_rgb(0,255,47)] bg-[#00ff2f] hover:shadow-[0_0_20px_rgb(0,255,47)] hover:scale-110 hover:bg-[#00ff2f] hover:text-black transition-all"
          >
            Submit
          </button>
        </div>
      </form>

      {/* Popup Component */}
      {isPopupVisible && (
        <div className="fixed inset-0 flex items-center justify-center bg-black bg-opacity-90 z-20">
          <div className="bg-[#004d1a] p-6 rounded-2xl scale-90 sm:scale-150 w-96 text-center">
            <h2 className="text-2xl font-bold text-[#00ff2f]">Thanks for submitting!</h2>
            <p className="text-lg text-white mt-4">We will get back to you soon.</p>
            <button
              onClick={closePopup}
              className="mt-4 px-6 py-2 bg-[#00ff2f] text-black rounded-md hover:bg-[#259c2b] transition-all"
            >
              Close
            </button>
          </div>
        </div>
      )}
    </div>
  );
}

export default Contact;
